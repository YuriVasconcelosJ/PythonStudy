x = 3.14
y = -4
z = 5

# A função round() arredonda um número de ponto flutuante para o inteiro mais próximo.
result = round(x)
# A função abs() retorna o valor absoluto de um número, ou seja, o número sem o sinal negativo.
result = abs(y)
# pow(base, exp) A função pow() calcula a potência de um número, onde:
result = pow(4, 3)
# max(*args) A função max() retorna o maior valor entre os argumentos fornecidos.
result = max(x , y, z)
#  min(*args) A função min() retorna o menor valor entre os argumentos fornecidos. Funciona de forma similar ao max().
result = min(x , y, z)
print(result)